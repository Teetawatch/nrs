function updateTranslations(lang) {
    const t = translations[lang];
    if (!t) return;

    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (!t[key]) return;

        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = t[key];
        } else if (el.hasAttribute('data-translate-placeholder')) {
            el.placeholder = t[key];
        } else {
            // Preserve child elements (e.g. SVG icons, span inside button)
            // Only update text nodes, not child elements
            const childNodes = Array.from(el.childNodes);
            const textNodes = childNodes.filter(n => n.nodeType === Node.TEXT_NODE);
            if (textNodes.length > 0) {
                textNodes.forEach(n => {
                    if (n.textContent.trim()) n.textContent = ' ' + t[key] + ' ';
                });
            } else {
                el.textContent = t[key];
            }
        }
    });

    // Update search inputs with placeholder attribute
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        if (t[key]) el.placeholder = t[key];
    });
}

function setLanguage(lang) {
    const thBtn = document.getElementById('lang-th');
    const enBtn = document.getElementById('lang-en');

    if (thBtn && enBtn) {
        if (lang === 'th') {
            thBtn.classList.remove('text-white/50'); thBtn.classList.add('text-white/80');
            enBtn.classList.remove('text-white/80'); enBtn.classList.add('text-white/50');
        } else {
            enBtn.classList.remove('text-white/50'); enBtn.classList.add('text-white/80');
            thBtn.classList.remove('text-white/80'); thBtn.classList.add('text-white/50');
        }
    }

    document.documentElement.lang = lang;
    document.body.classList.remove('lang-th', 'lang-en');
    document.body.classList.add('lang-' + lang);
    localStorage.setItem('language', lang);

    updateTranslations(lang);
}

document.addEventListener('DOMContentLoaded', function () {
    const savedLang = localStorage.getItem('language') || 'th';
    setLanguage(savedLang);
});
